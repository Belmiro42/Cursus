#include <unistd.h>

void	ft_print_comb(void)
{
	int		i;
	char	dec[3];
	char	spc[2];

	spc[0] = ',';
	spc[1] = ' ';
	i = 0;
	while (i != 999)
	{
		dec[0] = i / 100 + '0';
		dec[1] = (i % 100) / 10 + '0';
		dec[2] = (i % 10) + '0';
		if (dec[0] < dec[1] && dec[1] < dec[2])
		{
			write(1, &dec, 3);
			if (i != 789)
			{
				write(1, &spc, 2);
			}
		}
		i++;
	}
}

